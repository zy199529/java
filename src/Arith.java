public class Arith {
    public static void main(String[] args){
        float number1 = 45.56f;
        int number2 = 152;
        System.out.println("和："+(number1+number2));
        System.out.println("差："+(number1-number2));
        System.out.println("积："+(number1*number2));
        System.out.println("商："+(number1/number2));
    }
}
